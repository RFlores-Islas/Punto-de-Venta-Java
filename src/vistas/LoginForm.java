    public void validar(){
        String dni=txtPass.getText();
        String user=txtUser.getText();
        if(txtUser.getText().equals("") || txtPass.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Ingrese los datos correspondientes");
       txtUser.requestFocus();
        }
        else{
         ev=vdao.ValidarVendedor(dni, user);
         if(ev.getUser() != null && ev.getDni() !=null){
             Principal p= new Principal();
             p.setVisible(true);
             dispose();
         }
         else{
             JOptionPane.showMessageDialog(this, "Usuario invalido");
         txtUser.requestFocus();
         }
        }
    }
