     void nuevo(){
        txtNom.setText("");
        txtPre.setText("");
        txtStock.setText("");
        cboEstado.setSelectedItem(0);
        txtNomImagen.setText("");
        txtNom.requestFocus();
    }
     
     void limpiarTabla(){
        for(int i=0; i<modelo.getRowCount(); i++){
            modelo.removeRow(i);
            i=i-1;
        }
    }

