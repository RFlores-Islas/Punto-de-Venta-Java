    private void tablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaMouseClicked
         int fila = tabla.getSelectedRow();
       if(fila == -1){
           JOptionPane.showMessageDialog(this, "Seleccione una filaxx");
       }
       else{
           id = Integer.parseInt(tabla.getValueAt(fila,0).toString());
        String nom=tabla.getValueAt(fila,1).toString();
        String pre=tabla.getValueAt(fila,2).toString();
        String stock=tabla.getValueAt(fila,3).toString();
        String es =tabla.getValueAt(fila,4).toString();
        txtNom.setText(nom);
        txtPre.setText(pre);
        txtStock.setText(stock);
        cboEstado.setSelectedItem(es);
       }
    }//GEN-LAST:event_tablaMouseClicked

    private void btnImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImagenActionPerformed
        
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Formatos de archivos JPEG (*.JPG; *.JPEG)", "jpg","jpeg");
        JFileChooser archivo = new JFileChooser();
        archivo.addChoosableFileFilter(filtro);
        archivo.setDialogTitle("Abrir archivo");
        File ruta = new File("D:/Fotos");
        archivo.setCurrentDirectory(ruta);
        int ventana = archivo.showOpenDialog(null);
            if(ventana == JFileChooser.APPROVE_OPTION){
            File file = archivo.getSelectedFile();
            txtNomImagen.setText(String.valueOf(file));
             Image foto=getToolkit().getImage(txtNomImagen.getText());
             foto=foto.getScaledInstance(110, 110, Image.SCALE_DEFAULT);
             labeImagen.setIcon(new ImageIcon(foto));
        }
        
        
    }//GEN-LAST:event_btnImagenActionPerformed

